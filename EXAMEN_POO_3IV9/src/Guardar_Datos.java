    public void mostrar(){
    
        
        
        String matriz [][] = new String[lista.size()][3];
        
        for (int i = 0; i < lista.size(); i++) {
        
            matriz[i][0]=lista.get(i).getEdad();
            matriz[i][1]=lista.get(i).getPuntaje();
            matriz[i][2]=lista.get(i).getNombre();
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "Nombre", "Edad", "Puntaje"
            }
        ));
        
        
        
        
    }
    

